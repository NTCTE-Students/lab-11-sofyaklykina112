<?php
// 1. Создайте строку с номером кредитной карты
$card_number = '2200 7142 5415 7891';

// 2. Проверьте корректность номера кредитной карты
$pattern = "/^(?:4[0-9]{12}(?:[0-9]{3})?|5[1-5][0-9]{14}|3[47][0-9]{13}|6011[0-9]{12}|3(?:0[0-5]|[68][0-9])[0-9]{11}|7[0-9]{15})$/";
$result = preg_match($pattern, str_replace(' ', '', $card_number));
print('Номер кредитной карты ' . ($result ? 'корректен' : 'некорректен') . '<br>');
?>
